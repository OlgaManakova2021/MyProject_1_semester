# программа калькулятор

Mum1 = int(input('Введи первое число: '))
Mum2 = int(input('Введи второе число: '))
calc = input('Введи знак операции:  ')
if calc == '+':
    print(Mum1 + Mum2)
elif calc == '*':
    print(Mum1 * Mum2)
elif calc == '/':
    print(Mum1 / Mum2)
elif calc == '-':
    print(Mum1 - Mum2)


